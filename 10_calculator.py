#사칙 연산 +,-,/,*

#두 개의 숫자를 더해준 값을 리턴
def add(num1,num2):
    return num1 + num2

x = 3
y = 5

result = add(x,y)
print(f"두 숫자를 더한 결과는 {result} 입니다.")

def minus(num3,num4):
    return num3 - num4

a = 10
b = 5

result =  minus(a,b)
print(f"두 숫자를 뺀 결과는 {result}입니다.")

def multiply(num5,num6):
    return num5 * num6

c = 3
d = 5

result = multiply(c,d)
print(f"두 숫자를 곱한 결과는 {result}입니다.")
def divide(num7,num8):
    return num7 / num8

e = 42
f = 7

result = divide(e,f)
print(f"두 숫자를 곱한 결과는 {result}입니다.")
